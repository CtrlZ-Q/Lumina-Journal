// 每个类别分成4包，每包250条
function splitPack(allQuotes, id, name, icon, price) {
  const packs = []
  for (let i = 0; i < 4; i++) {
    packs.push({
      id: `${id}_${i + 1}`,
      name: `${name}·${['壹', '贰', '叁', '肆'][i]}`,
      icon,
      category: 'quote',
      type: 'collectible',
      price,
      data: { quotes: allQuotes.slice(i * 250, (i + 1) * 250) },
    })
  }
  return packs
}

